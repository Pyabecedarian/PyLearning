"""
len(), min(), max() --- useful built-in functions

1>  len():
        returns the number of elements a sequence contains

2>  min() / max()
        returns the smallest and largest elements of the sequence, respectively

"""

numbers = [100, 34, 678]

print(len(numbers))  # how many elements contained
print(max(numbers))  # retrieve the maximum element
print(min(numbers))  # retrieve the minimum element
